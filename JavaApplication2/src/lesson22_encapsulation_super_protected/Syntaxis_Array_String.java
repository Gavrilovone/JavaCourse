package lesson22_encapsulation_super_protected;

public class Syntaxis_Array_String {
    public static void main(String[] args) {
        int i = 2;
        String s = new String[]{"A", "B", "C", "D"}[i]; // означает, что переменная s = 2й элемент этого массива
        System.out.println(s); // С
    }
}
